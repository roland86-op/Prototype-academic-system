    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        // TODO add your handling code here:
        try {
            koneksi();
            String sql = "Select * from PeminjamanMobil where Nopinjam='"
                    + jTextField1.getText() + "'";
            ResultSet rs = cn.executeQuery(sql);
            if (rs.next()) {
                jDateChooser1.setDate(rs.getDate(2));
                jTextField3.setText(rs.getString(3));
                jComboBox1.setSelectedItem(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jTextField6.setText(rs.getString(6));
                jTextField7.setText(rs.getString(7));
                jTextField8.setText(rs.getString(8));
                Simpan.setVisible(false);
            } else {
                Simpan.setVisible(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan " + e);
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        try {
            koneksi();
            String sql = "insert into PeminjamanMobil values('"
                    + jTextField1.getText() + "','"
                    + ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText() + "','"
                    + jTextField3.getText() + "','"
                    + jComboBox1.getSelectedItem() + "','"
                    + jTextField5.getText() + "','"
                    + jTextField6.getText() + "','"
                    + jTextField7.getText() + "','"
                    + jTextField8.getText() + "')";
            cn.executeUpdate(sql);
            conn.close();
            tampil();
            bersih();
            JOptionPane.showMessageDialog(null, "Data berhasil di simpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Proses penyimpanan gagal" + e);
        }
    }//GEN-LAST:event_SimpanActionPerformed

