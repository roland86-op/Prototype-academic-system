    public void tampil() {
        DefaultTableModel tabela = new DefaultTableModel();
        tabela.addColumn("No Pinjam");
        tabela.addColumn("Tanggal Pinjam");
        tabela.addColumn("Nama Peminjam");
        tabela.addColumn("Jenis Mobil");
        tabela.addColumn("Sewa per hari");
        tabela.addColumn("No Polisi");
        tabela.addColumn("Lama Pinjam");
        tabela.addColumn("Total Bayar");

        try {
            koneksi();
            String sql = "Select * from PeminjamanMobil";
            ResultSet rs = cn.executeQuery(sql);
            while (rs.next()) {
                tabela.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8),});
            }
            tabel.setModel(tabela);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan" + e);
        }
    }

