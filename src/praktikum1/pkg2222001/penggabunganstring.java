public class penggabunganstring {
        public static void main(String[]args){
            //Dua string dapat disambung menjadi satu string panjang
            System.out.println("Berikut disajikan beberapa fakta kehidupan yang"+" perlu Anda Ketahui :");
            System.out.println();
            
            //Sebuah String dapat memuat dijit - dijit
            System.out.println("Jumlah huruf dalam alphabet Hawaiin :12");
            
            //Sebuah nilai numeric dapat disambung dengan string
            System.out.println("Kode Telpon Antartica :+672");
            System.out.println("Tahun dimana Leonardo da Vinci menciptakan"+" the parachute : +1515");
            System.out.println("Terdapat : "+24+" Jam Sehari");
        }
    }
